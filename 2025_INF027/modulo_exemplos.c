#include <stdio.h>
#include <stdlib.h>

int main(){
    // a operação % (módulo) te dá o resto da 
    // divisão de um número por outro
    // assim
    // 2%10 representa o resto da divisão de 2 por 10 que é 2
    // 
    int r = (1+1)%10;
    printf("do 1 vou para o %d\n", r);

    r = (9+1) %10;
    printf("do 9 vou para o %d\n", r);

}